# Integer

tea_grams = 10
ginger_grams = 3

total_grams = tea_grams + ginger_grams # addition (add the two numbers)
print(f"addition: {total_grams}")

# subtraction (subtract the two numbers)
total_grams = tea_grams - ginger_grams
print(f"subtraction: {total_grams}")

# multiplication (multiply the two numbers)
total_grams = tea_grams * ginger_grams
print(f"multiplication: {total_grams}")

# division
total_grams = tea_grams / ginger_grams
print(f"division: {total_grams}")

# modulus (remainder)
total_grams = tea_grams % ginger_grams
print(f"modulus: {total_grams}")

# floor division (remove the decimal part)
total_grams = tea_grams // ginger_grams
print(f"floor division: {total_grams}")

# power
total_grams = tea_grams ** ginger_grams
print(f"power: {total_grams}")

# underscore for readability
total_tea_harvest = 10_100_100_000_00000
print(f"total tea harvest: {total_tea_harvest}")

